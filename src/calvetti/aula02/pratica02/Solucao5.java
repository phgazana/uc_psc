public class Solucao5 {
    public static void main(String[] args) {

        /*5) Construir um algoritmo que indique se o número digitado está compreendido
entre 20 e 90 ou não.*/

        int numero = 100;

        if (numero >= 20 && numero <= 90) {
            System.out.println("Está compreendido entre 20 e 90");
        } else {
            System.out.println("Não está compreendido entre 20 e 90");
        }
    }
}
